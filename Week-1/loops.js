// There are 3 types of loops in JS
// 1. for loop
// 2. while loop
// 3. do while loop

// For loop
for(let i =0; i<5;i++){
    console.log(i)
}

// While loop
let j=0;
while(j<5){
    console.log(j);
    j++;
}

// Finding sum of first 100 natural numbers
var sum = 0 
for(var x = 0; x<100;x++){
    sum += x;
}
console.log(sum)
